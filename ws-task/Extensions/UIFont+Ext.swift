import UIKit

extension UIFont {
    static let regular16 = UIFont.systemFont(ofSize: 16)
    static let regular10 = UIFont.systemFont(ofSize: 10)
    static let bold16 = UIFont.systemFont(ofSize: 16, weight: .bold)
    static let black16 = UIFont.systemFont(ofSize: 16, weight: .black)
}
